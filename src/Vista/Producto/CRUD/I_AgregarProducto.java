    private void checkAdicionar() {
        if (chxAdicionar.isSelected()) {
            accionInsecion = E_TipoAccion.AUMENTAR;
            cp.desabilitarControles(this);
            cp.enableControl(txtStock);
            cp.enableControl(txtIDBusqueda);
            cp.enableControl(btnBuscar_NuevaBusqueda);
            cp.setContent("Actualizar producto", btnAgregar_Aumentar_Producto);
            cp.setContent("Stock a adicionar:", lblStock);
            cp.focus(txtIDBusqueda);
        } else {
            accionInsecion = E_TipoAccion.AGREGAR;
            String campo = txtIDBusqueda.getText();
            cp.clearCamposAgregarP(this);
            txtIDBusqueda.setText(campo);
            cp.habilitarControles(this);
            cp.disableControl(txtIDBusqueda);
            cp.disableControl(btnBuscar_NuevaBusqueda);
            cp.setContent("Agregar producto", btnAgregar_Aumentar_Producto);
            cp.setContent("Stock:", lblStock);
            cp.focus(txtNombre);
        }
    }

