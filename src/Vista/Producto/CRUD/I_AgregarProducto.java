    private void btnAgregar_Aumentar_ProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregar_Aumentar_ProductoActionPerformed
        mp = cp.asignarCamposAObjetoAgregarP(this);
        switch (accionInsecion) {
            case AGREGAR:
                int rowsAfected = cp.agregar(mp);
                if (rowsAfected > 0) {
                    cp.clearCamposAgregarP(this);
                    //cp.
                } else if (rowsAfected == -1) {
                    if (JOptionPane.YES_OPTION == Mensaje.CONFIRMACION("Este item ya existe. ¿Desea adicionar stock al item?")) {
                        cp.setEstablish(true, chxAdicionar);
                        checkAdicionar();
                        cp.setContent(cp.consultar(mp.getNombreProducto()).getIdProducto(), txtIDBusqueda);
                        Buscar_NuevaBusqueda();
                    }
                }
                break;

            case AUMENTAR:
                mp.setStockProducto(stockActual + cp.numberEntero(txtStock));
                if (cp.aumentar(mp) > 0) {
                    cp.clearCamposAgregarP(this);
                    Buscar_NuevaBusqueda();
                    cp.setEstablish(false, chxAdicionar);
                    checkAdicionar();
                    
                }
                break;
        }
    }//GEN-LAST:event_btnAgregar_Aumentar_ProductoActionPerformed

