    private void Buscar_NuevaBusqueda() {
        switch (accionBusqueda) {
            case BUSCAR:
                mp = cp.consultar(txtIDBusqueda);
                if (mp != null) {
                    cp.asignarCamposFormAgregarP(this, mp);
                    cp.clear(txtStock);
                    stockActual = mp.getStockProducto();
                    cp.disableControl(txtIDBusqueda);
                    cp.setContent("Nueva Busqueda", btnBuscar_NuevaBusqueda);
                    cp.setContent("Actualizar producto", btnAgregar_Aumentar_Producto);
                    cp.focus(txtStock);
                    
                    accionBusqueda = E_TipoAccion.NUEVA_BUSQUEDA;
                }
                mp = new MProducto();
                break;

            case NUEVA_BUSQUEDA:
                cp.clearCamposAgregarP(this);
                cp.enableControl(txtIDBusqueda);
                cp.setContent("Buscar", btnBuscar_NuevaBusqueda);
                cp.setContent("Agregar producto", btnAgregar_Aumentar_Producto);
                
                cp.focus(txtIDBusqueda);
                
                accionBusqueda = E_TipoAccion.BUSCAR;
                break;
        }
    }

