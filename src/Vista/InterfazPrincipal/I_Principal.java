    /**
     * Creates new form Principal
     */
    public void Agregar_I_Principal() {
        if (acceso == E_Acceso.LOGIN) {
            Interaccion.Login_ShowJPanel(new I_Login(this), contenedor, this);
        } else {
            switch (user.getIdTipoUsuario()) {
                case 1 -> {
                    menuAdmin = new I_Admin(this);
                    acceso = E_Acceso.ADMIN;
                    Interaccion.Principal_ShowJPanel(menuAdmin, contenedor, this, btnCerrarSesion);
                }
                case 2 -> {
                    menuVendedor = new I_Vendedor(this);
                    acceso = E_Acceso.EMPLEADO;
                    Interaccion.Principal_ShowJPanel(menuVendedor, contenedor, this, btnCerrarSesion);
                }

                default ->
                    JOptionPane.showMessageDialog(this, "No se encuentra el Form.");
            }
        } 
    }

